    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        boolean check1=true;
        if(kt()){
        ChucVuDTO cv= new ChucVuDTO();
        cv.setMaCV(txMaCV.getText());
        cv.setTenChucVu(txTen.getText());
        for(int i=0;i<bus.dscv.size();i++){
            if(bus.dscv.get(i).getMaCV().toLowerCase().trim().equals(txMaCV.getText().toLowerCase().trim())){
                check1=false;
                JOptionPane.showMessageDialog(rootPane, "Trùng mã chức vụ");
                txMaCV.setText("");
                txMaCV.requestFocus();
                break;
            }
            else
            {
                check1=true;
            }
        }
        if(check1==true){
        int res=JOptionPane.showConfirmDialog(rootPane, "Bạn muốn thêm chức vụ này?");
        if(res==JOptionPane.YES_OPTION){
            if(bus.themchucvu(cv)){
                showResult();
                reset();
                JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
            }else{
                JOptionPane.showMessageDialog(rootPane, "Thêm thất bại");
            }
        }
        }
        }else{
                JOptionPane.showMessageDialog(rootPane, "Thêm thất bại");
            }
    }//GEN-LAST:event_btnThemActionPerformed

   
