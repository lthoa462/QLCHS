    public boolean kt(){
        boolean check=true;
        
        if(txMaCV.getText().length()==0){
            JOptionPane.showMessageDialog(rootPane, "Mã chức vụ còn trống");
            check=false;
        }
        
        if(txTen.getText().length()==0){
            JOptionPane.showMessageDialog(rootPane, "Tên chức vụ còn trống");
            check=false;
        }
        return check;
    }
    public void showResult(){
        ChucVuDTO cv= bus.dscv.get(bus.dscv.size()-1);
         mode.addRow(new Object[]{
                 cv.getMaCV(),cv.getTenChucVu()
             });
    }
    
    public void showSearchID(){
        mode.setRowCount(0);
        for(ChucVuDTO cv: bus.timKiemID(txInfo.getText().toLowerCase())){
            mode.addRow(new Object[]{
                 cv.getMaCV(),cv.getTenChucVu()
             });
        }
    }
    public void showSearchTen(){
        
        mode.setRowCount(0);
        for(ChucVuDTO cv: bus.timKiemten(txInfo.getText().toLowerCase())){
            mode.addRow(new Object[]{
                 cv.getMaCV(),cv.getTenChucVu()
             });
        }
    }
    public ChucVuDTO getFromTF(){
        ChucVuDTO cv= new ChucVuDTO();
        cv.setMaCV(txMaCV.getText());
        cv.setTenChucVu(txTen.getText());
        return cv;
    }
    
     public void reset(){
        txMaCV.setEnabled(true);
        txMaCV.setText("");
        txTen.setText("");
        btnSearch.setEnabled(true);
    }
