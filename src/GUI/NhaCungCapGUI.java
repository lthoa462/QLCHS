    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
         int i=tbl.getSelectedRow();
        if(kt()){
        NhaCungCapDTO ncc1= getFromTF();
        if(i>=0){
         int res= JOptionPane.showConfirmDialog(rootPane, "Bạn muốn sửa nhà cung cấp này ?");
           if(res==JOptionPane.YES_OPTION){
               if(bus.suancc(ncc1)){
                   NhaCungCapDTO ncc= bus.docncc().set(i, ncc1);
                    mode.setValueAt(ncc1.getMaCC(), i, 0);
                    mode.setValueAt(ncc1.TenCC, i, 1);
                    reset();
                    JOptionPane.showMessageDialog(rootPane,"Sửa thành công");
               }
               else{
                    JOptionPane.showMessageDialog(rootPane,"Sửa thất bại");
               }
           }
    }}
       else{
                    JOptionPane.showMessageDialog(rootPane,"Sửa thất bại");
               } 
    }//GEN-LAST:event_btnSuaActionPerformed

