     public boolean kt(){
        boolean check=true;
        
        if(txMaCC.getText().length()==0){
            JOptionPane.showMessageDialog(rootPane, "Mã nhà cung cấp còn trống");
            check=false;
        }
        
        if(txTen.getText().length()==0){
            JOptionPane.showMessageDialog(rootPane, "Tên nhà cung cấp còn trống");
            check=false;
        }
        return check;
    }
    public void showResult(){
        NhaCungCapDTO ncc= bus.dsncc.get(bus.dsncc.size()-1);
         mode.addRow(new Object[]{
                 ncc.getMaCC(),ncc.getTenCC()
             });
    }
    
    public void showSearchID(){
        mode.setRowCount(0);
        for(NhaCungCapDTO ncc: bus.timKiemID(txSearch.getText().toLowerCase())){
            mode.addRow(new Object[]{
                 ncc.getMaCC(),ncc.getTenCC()
             });
        }
    }
    public void showSearchTen(){
        
        mode.setRowCount(0);
        for(NhaCungCapDTO ncc: bus.timKiemten(txSearch.getText().toLowerCase())){
            mode.addRow(new Object[]{
                 ncc.getMaCC(),ncc.getTenCC()
             });
        }
    }
    public NhaCungCapDTO getFromTF(){
        NhaCungCapDTO ncc= new NhaCungCapDTO();
        ncc.setMaCC(txMaCC.getText());
        ncc.setTenCC(txTen.getText());
        return ncc;
    }
    
     public void reset(){
        txMaCC.setEnabled(true);
        txMaCC.setText("");
        txTen.setText("");
        btnTim.setEnabled(true);
        cbSearch.setSelectedIndex(0);
    }
