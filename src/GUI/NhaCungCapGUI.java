    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
         boolean check1=true;
        if(kt()){
        NhaCungCapDTO ncc= new NhaCungCapDTO();
        ncc.setMaCC(txMaCC.getText());
        ncc.setTenCC(txTen.getText());
        for(int i=0;i<bus.dsncc.size();i++){
            if(bus.dsncc.get(i).getMaCC().toLowerCase().trim().equals(txMaCC.getText().toLowerCase().trim())){
                check1=false;
                JOptionPane.showMessageDialog(rootPane, "Trùng mã nhà cung cấp");
                txMaCC.setText("");
                txMaCC.requestFocus();
                break;
            }
            else
            {
                check1=true;
            }
        }
        if(check1==true){
        int res=JOptionPane.showConfirmDialog(rootPane, "Bạn muốn thêm nhà cung cấp này?");
        if(res==JOptionPane.YES_OPTION){
            if(bus.themncc(ncc)){
                showResult();
                reset();
                JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
            }else{
                JOptionPane.showMessageDialog(rootPane, "Thêm thất bại");
            }
        }
        }
        }else{
                JOptionPane.showMessageDialog(rootPane, "Thêm thất bại");
            }
    }//GEN-LAST:event_btnThemActionPerformed

