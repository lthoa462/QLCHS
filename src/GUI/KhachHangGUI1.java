    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        if(check()){
            
        KhachHangDTO kh1=textfield();
        
       int i=tb1.getSelectedRow();
       if(i>=0){
         int res=JOptionPane.showConfirmDialog(rootPane, "Bạn muốn sửa khách hàng này?");
         if(res==JOptionPane.YES_OPTION){
             if(bus.changekh(kh1)){
                 KhachHangDTO kh=bus.readdskh().set(i, kh1);
                 model.setValueAt(kh1.getMa(),i,0);
                 model.setValueAt(kh1.getHo(),i,1);
                 model.setValueAt(kh1.getTen(),i,2);
                 model.setValueAt(kh1.getNgaysinh(),i,3);
                 model.setValueAt(kh1.getGender(),i,4);
                 model.setValueAt(kh1.getDiachi(),i,5);
                 model.setValueAt(kh1.getSdt(),i,6);
                 reset();
                 JOptionPane.showMessageDialog(rootPane,"Sửa thành công");
             }
             else
                 JOptionPane.showMessageDialog(rootPane,"Sửa thất bại");
           }
         
        }else
           JOptionPane.showMessageDialog(rootPane,"Sửa Thất bại");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
       KhachHangDTO kh=new KhachHangDTO();
       boolean ischeck6=true;
       kh.setMa(txMaKH.getText());
       kh.setHo(txHo.getText());
       kh.setTen(txTen.getText());
       try {
            kh.setNgaysinh(new SimpleDateFormat("yyyy-MM-dd").format(txDate.getDate()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane,"Bạn chưa chọn ngày");
            ischeck6=false;
        }
       if(raNam.isSelected()){
           kh.setGender("Nam");
       }
       else{
        kh.setGender("Nữ");
    }
      kh.setDiachi(txDiaChi.getText());
      kh.setSdt(txSdt.getText());
        
      
     if(ischeck6)
     {  
         String ma=txMaKH.getText();
         int i=bus.kiemtratrung(ma);
     if(i==1&&bus.addkh(kh)){
         check();
         bus.dskh.add(kh);
         setrow(kh);
         reset();
         JOptionPane.showMessageDialog(rootPane,"Thêm thành công");
     }
     else{
         JOptionPane.showMessageDialog(rootPane,"Trùng Mã khách hàng");
     }
     }
     else{
         JOptionPane.showMessageDialog(rootPane,"Thêm thất bại");
     }
    }//GEN-LAST:event_btnThemActionPerformed

