    public KhachHangDTO textfield(){
        KhachHangDTO kh= new KhachHangDTO();
         kh.setMa(txMaKH.getText());
         kh.setHo(txHo.getText());
         kh.setTen(txTen.getText());
         kh.setNgaysinh(new SimpleDateFormat("yyyy-MM-dd").format(txDate.getDate()));
         if(raNam.isSelected()){
             kh.setGender("Nam");
         }
         if(raNu.isSelected()){
             kh.setGender("Nữ");
         }
         kh.setDiachi(txDiaChi.getText());
         kh.setSdt(txSdt.getText());
         return kh;
    }
