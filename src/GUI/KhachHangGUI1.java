    public boolean check(){
        KhachHangDTO kh=new KhachHangDTO();
        boolean ischeck1=true;
        if(txMaKH.getText().length()==0){
        JOptionPane.showMessageDialog(rootPane,"Bạn chưa nhập Mã Khách Hàng");
        ischeck1=false;
      }  
      
      if(txHo.getText().length()==0){
          JOptionPane.showMessageDialog(rootPane,"Bạn chưa nhập họ!!");
          ischeck1=false;
          
      }
      if(txTen.getText().length()==0){
         JOptionPane.showMessageDialog(rootPane,"Bạn chưa nhập tên"); 
          ischeck1=false;
          
      }
      if(txDiaChi.getText().length()==0){
         JOptionPane.showMessageDialog(rootPane,"Bạn chưa nhập địa chỉ"); 
         ischeck1=false;
         
      }
     
      if(txSdt.getText().length()>10){
         JOptionPane.showMessageDialog(rootPane,"SĐT không vượt quá 10 số");
          ischeck1=false;
          
      }
      if(txSdt.getText().length()==0){
          JOptionPane.showMessageDialog(rootPane,"Bạn chưa nhập SĐT");
          ischeck1=false;
      }
      return ischeck1;
    }
    

