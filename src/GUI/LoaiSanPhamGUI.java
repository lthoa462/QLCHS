     public boolean kt(){
        boolean check=true;
        
        if(txMaLoai.getText().length()==0){
            JOptionPane.showMessageDialog(rootPane, "Mã loại sản phẩm còn trống");
            check=false;
        }
        
        if(txTenLoai.getText().length()==0){
            JOptionPane.showMessageDialog(rootPane, "Tên loại sản phẩm còn trống");
            check=false;
        }
        return check;
    }
    public void showResult(){
        LoaiSanPhamDTO sp= bus.dslsp.get(bus.dslsp.size()-1);
         mode.addRow(new Object[]{
                 sp.getMaLoai(),sp.getTenLoai()
             });
    }
    
    public void showSearchID(){
        mode.setRowCount(0);
        for(LoaiSanPhamDTO sp: bus.timKiemID(txSearch.getText().toLowerCase())){
            mode.addRow(new Object[]{
                 sp.getMaLoai(),sp.getTenLoai()
             });
        }
    }
    public void showSearchTen(){
        
        mode.setRowCount(0);
        for(LoaiSanPhamDTO sp: bus.timKiemten(txSearch.getText().toLowerCase())){
            mode.addRow(new Object[]{
                 sp.getMaLoai(),sp.getTenLoai()
             });
        }
    }
    public LoaiSanPhamDTO getFromTF(){
        LoaiSanPhamDTO sp= new LoaiSanPhamDTO();
        sp.setMaLoai(txMaLoai.getText());
        sp.setTenLoai(txTenLoai.getText());
        return sp;
    }
    
     public void reset(){
        txMaLoai.setEnabled(true);
        txMaLoai.setText("");
        txTenLoai.setText("");
         btnSeach.setEnabled(true);
    }
