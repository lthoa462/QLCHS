    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
         boolean check1=true;
        if(kt()){
        LoaiSanPhamDTO lsp= new LoaiSanPhamDTO();
        lsp.setMaLoai(txMaLoai.getText());
        lsp.setTenLoai(txTenLoai.getText());
        for(int i=0;i<bus.dslsp.size();i++){
            if(bus.dslsp.get(i).getMaLoai().toLowerCase().trim().equals(txMaLoai.getText().toLowerCase().trim())){
                check1=false;
                JOptionPane.showMessageDialog(rootPane, "Trùng mã loại sản phẩm");
                txMaLoai.setText("");
                txMaLoai.requestFocus();
                break;
            }
            else
            {
                check1=true;
            }
        }
        if(check1==true){
        int res=JOptionPane.showConfirmDialog(rootPane, "Bạn muốn thêm loại sản phẩm này?");
        if(res==JOptionPane.YES_OPTION){
            if(bus.themloaisp(lsp)){
                showResult();
                reset();
                JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
            }else{
                JOptionPane.showMessageDialog(rootPane, "Thêm thất bại");
            }
        }
        }
        }else{
                JOptionPane.showMessageDialog(rootPane, "Thêm thất bại");
            }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int i=tbl.getSelectedRow();
        if(kt()){
        LoaiSanPhamDTO lsp1= getFromTF();
        if(i>=0){
         int res= JOptionPane.showConfirmDialog(rootPane, "Bạn muốn sửa loại sản phẩm này ?");
           if(res==JOptionPane.YES_OPTION){
               if(bus.sualoaisp(lsp1)){
                   LoaiSanPhamDTO lsp= bus.doclsp().set(i, lsp1);
                    mode.setValueAt(lsp1.getMaLoai(), i, 0);
                    mode.setValueAt(lsp1.getTenLoai(), i, 1);
                    reset();
                    JOptionPane.showMessageDialog(rootPane,"Sửa thành công");
               }
               else{
                    JOptionPane.showMessageDialog(rootPane,"Sửa thất bại");
               }
           }
    }}
       else{
                    JOptionPane.showMessageDialog(rootPane,"Sửa thất bại");
               } 
    }//GEN-LAST:event_btnSuaActionPerformed

